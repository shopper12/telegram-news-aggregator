from __future__ import annotations

import hashlib


def pick_variant(key: str, category: str = "general") -> tuple[str, str]:
    seed = int(hashlib.sha256(key.encode("utf-8")).hexdigest(), 16)
    actions = {
        "money": [
            "오늘은 새 결정보다 현재 선택의 기준을 3줄로 적고, 넘지 말아야 할 한계를 먼저 정하라.",
            "이번 주에는 후보를 늘리지 말고, 유지할 것 1개와 정리할 것 1개를 분리하라.",
            "바로 움직이기보다 10분 대기 규칙을 두고, 충동이 줄어든 뒤 같은 판단인지 확인하라.",
            "관심 대상을 3개 이하로 줄이고, 이유가 설명되지 않는 선택은 보류하라.",
        ],
        "relationship": [
            "오늘은 설득보다 관찰을 우선하라. 상대의 말 1개보다 반복 행동 1개를 기준으로 판단하라.",
            "이번 주 대화는 요구사항을 1개로 줄이고, 실제로 바뀔 행동을 날짜와 함께 정하라.",
            "감정 확인을 길게 하기보다 시간·역할·미래계획 중 하나를 구체적으로 합의하라.",
            "상대 반응을 시험하지 말고, 네가 지킬 수 있는 경계선 1개를 차분히 말하라.",
        ],
        "work": [
            "오늘 업무는 자료수집을 멈추고 제출 가능한 초안 1개를 만드는 쪽으로 몰아라.",
            "이번 주에는 투입시간보다 산출물 개수로 평가하라. 40분 단위 결과물을 남겨라.",
            "설명할 쟁점을 3줄로 줄이고, 근거자료는 뒤에 붙이는 구조로 정리하라.",
            "완벽한 설계보다 마감 가능한 버전을 먼저 내고, 피드백을 받아 2차 수정하라.",
        ],
        "general": [
            "오늘 할 일은 3개가 아니라 1개다. 완료 기준이 숫자로 보이는 것만 선택하라.",
            "이번 주에는 새 결심을 추가하지 말고, 이미 하던 것 중 효과 없는 루틴 1개를 끊어라.",
            "감정이 강한 선택은 하루 유예하고, 비용·시간·회복가능성을 표로 적은 뒤 결정하라.",
            "사람에게 인정받는 목표보다 실제로 삶을 개선하는 행동 1개를 먼저 처리하라.",
        ],
    }
    cautions = [
        "확신이 강할수록 반대 근거 1개를 먼저 확인해야 한다.",
        "피로한 날의 결정은 과잉통제와 회피가 섞이기 쉽다.",
        "남의 속도와 비교하면 판단이 급해진다. 본인 기준표를 먼저 써라.",
        "말로 정리된 계획보다 실제 캘린더에 들어간 행동만 믿어라.",
    ]
    pool = actions.get(category, actions["general"])
    return pool[seed % len(pool)], cautions[(seed // 11) % len(cautions)]
